class TheRoundCityDiv2 {
public:
	int find(int r) {
		
		int ans = -1; //(0,0) will be counted
		for(int i=-r;i<=r;++i)
			for(int j=-r;j<=r;++j)
				if(i*i+j*j<=r*r)
					ans++;
				
		return ans;
	}
};

